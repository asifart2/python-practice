# Bitwise Operatiors
x = 5 # Binary: 0101
y = 3 #Binary: 0011
print(x & y) #  when both bits are 1, the result is 1, otherwise it's 0 . because 0101 & 0011 = 0001 (which is 1 in decimal) 
print(x | y) # when either bit is 1, the result is 1, otherwise it's 0. because 0101 | 0011 = 0111 (which is 7 in decimal)
print(x ^ y) # when the bits are different, the result is 1, otherwise it's 0. because 0101 ^ 0011 = 0110 (which is 6 in decimal)
print(~x)
print(x >> 1)
print(x << 1)
